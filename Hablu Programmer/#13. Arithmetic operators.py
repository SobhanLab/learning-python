# Arithmetic operators
a = 5
b = 23
x = 10
print("Addition: a + b = ", a + b)
print("Subtraction: b - a = ", b - a)
print("Multiplication: a * b = ", a * b)
print("Division: b / a = ", b / a)
print("Floor Division: b // a = ", b // a)
print("Modulus: b % a = ", b % a)
print("Exponentiation: x ** 3 = ", x ** 3)
